uint8_t Comm485_2_Rx_Buff[1024];
uint8_t Comm485_2_Tx_Buff[1024];

/*
----------------------------------------------------------------------------------------------

----------------------------------------------------------------------------------------------
*/
void Comm485_2_Task(void)
{
    for(int i = 0; i < 1024; i++)
    {
        Comm485_2_Tx_Buff[i] = i;
    }

    while(1)
    {
        osDelay(1000);
        _485_2_Tx_And_Rx(Comm485_2_Tx_Buff, 20, Comm485_2_Rx_Buff, 1024);
    }
}

